//***** all about numbers   ****/ 


//adding number       (+)
var sum = 10 + 10;
console.log(sum);


//subtracting number      (-)
var difference = 30 - 15;
console.log(difference);


var subtractingNumber = 50 - 30;
console.log(subtractingNumber);

//multiplying number       (*)
var product = 2.0 * 2.5;
 console.log(product);

var number2 = 50 * 50;
console.log(number2);

// dividing number          (/)
var  quotient = 100 /  25;
console.log(quotient);

// remainder operator    (%)
let divide = 11 % 3;
console.log(divide);




//incrementing number    myBar+1 = myBar++; (++)
let myBar = 12; 
myBar++;
console.log(myBar);

let you = 22;
you++;
console.log(you);

//decrementing number
var myAge = 19;
myAge --;
console.log(myAge);



var myNewAge = 20;

myNewAge--;

console.log(myNewAge);




